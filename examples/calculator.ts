// Simple calculator with variables
let calcA = 10;
let calcB = 5;

print("Addition: " + calcA + " + " + calcB + " = " + (calcA + calcB));
print("Subtraction: " + calcA + " - " + calcB + " = " + (calcA - calcB));
print("Multiplication: " + calcA + " * " + calcB + " = " + (calcA * calcB));
print("Division: " + calcA + " / " + calcB + " = " + (calcA / calcB));
print("Modulo: " + calcA + " % " + calcB + " = " + (calcA % calcB));